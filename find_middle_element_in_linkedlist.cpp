#include <bits/stdc++.h>
using namespace std;

struct Node
{
    int data;
    struct Node* next;
    
    Node(int x){
        data = x;
        next = NULL;
    }
};
void printList(Node* node) 
{ 
    while (node != NULL) { 
        cout << node->data <<" "; 
        node = node->next; 
    }  
    cout<<"\n";
} class Solution{
    public:
    /* Should return data of middle node. If linked list is empty, then  -1*/
    int getMiddle(Node *head)
    {
        // Your code here
        Node* fast=head;
        Node* slow=head;
        while(fast!=NULL && fast->next!=NULL&& slow!=NULL)
        {
            fast=fast->next->next;
            slow=slow->next;
        }
        return slow->data;
    }
};
